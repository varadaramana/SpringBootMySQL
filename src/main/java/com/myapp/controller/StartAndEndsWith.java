package com.myapp.controller;

public class StartAndEndsWith {

	public static void main(String[] args) {
		String S1 = "My name is matta ganesh";
		System.out.println(S1.startsWith("My"));
		System.out.println(S1.startsWith("is"));
		System.out.println(S1.endsWith("ganesh"));
		System.out.println(S1.endsWith("matta"));
	}

}
